const fs = require('fs');
const path = require('path');

const PLUGINS_DIR = path.join(__dirname, '..', 'plugins');
const APP_DIR = path.join(__dirname, '..', 'app');
const GENERATED_MARKER = '// @generated by generate-plugin-routes.js — do not edit\n';
const MANIFEST_FILE = path.join(__dirname, '..', '.plugin-routes.json');
const MANIFESTS_FILE = path.join(PLUGINS_DIR, '_manifests.ts');

function loadManifest(pluginDir) {
  const manifestPath = path.join(pluginDir, 'manifest.ts');
  const manifestJsPath = path.join(pluginDir, 'manifest.js');
  if (fs.existsSync(manifestJsPath)) {
    return require(manifestJsPath).default || require(manifestJsPath);
  }
  if (fs.existsSync(manifestPath)) {
    const content = fs.readFileSync(manifestPath, 'utf8');
    return parseManifestRoutes(content, pluginDir);
  }
  return null;
}

function parseManifestRoutes(content, pluginDir) {
  const pluginName = path.basename(pluginDir);
  const routes = { pages: {}, api: {} };

  const pageMatches = content.matchAll(
    /['"](\/.+?)['"]\s*:\s*\(\)\s*=>\s*import\(['"]\.\/pages\/(.+?)['"]\)/g
  );
  for (const m of pageMatches) {
    routes.pages[m[1]] = `@/plugins/${pluginName}/pages/${m[2]}`;
  }

  const apiMatches = content.matchAll(
    /['"](\/.+?)['"]\s*:\s*\(\)\s*=>\s*import\(['"]\.\/api\/(.+?)['"]\)/g
  );
  for (const m of apiMatches) {
    routes.api[m[1]] = `@/plugins/${pluginName}/api/${m[2]}`;
  }

  return routes;
}

function generatePageShim(route, importPath) {
  const dir = path.join(APP_DIR, route);
  fs.mkdirSync(dir, { recursive: true });
  const content = `${GENERATED_MARKER}export { default } from '${importPath}';\n`;
  fs.writeFileSync(path.join(dir, 'page.tsx'), content);
}

function generateApiShim(route, importPath) {
  const dir = path.join(APP_DIR, route);
  fs.mkdirSync(dir, { recursive: true });
  const content = `${GENERATED_MARKER}export { GET, POST, PUT, DELETE, PATCH } from '${importPath}';\n`;
  fs.writeFileSync(path.join(dir, 'route.ts'), content);
}

function cleanPreviousRoutes() {
  if (!fs.existsSync(MANIFEST_FILE)) return;
  const prev = JSON.parse(fs.readFileSync(MANIFEST_FILE, 'utf8'));
  for (const route of prev.generatedRoutes || []) {
    const fullPath = path.join(APP_DIR, route);
    if (fs.existsSync(fullPath)) {
      const content = fs.readFileSync(fullPath, 'utf8');
      if (content.startsWith(GENERATED_MARKER)) {
        fs.unlinkSync(fullPath);
        const dir = path.dirname(fullPath);
        try {
          fs.rmdirSync(dir);
        } catch {}
      }
    }
  }
}

function generateManifestsFile(pluginNames) {
  if (pluginNames.length === 0) {
    const content = `${GENERATED_MARKER}import type { PluginManifest } from './registry';\n\nexport const manifests: PluginManifest[] = [];\n`;
    fs.writeFileSync(MANIFESTS_FILE, content);
    return;
  }

  const imports = pluginNames
    .map((name, i) => `import plugin${i} from './${name}/manifest';`)
    .join('\n');
  const arr = pluginNames.map((_, i) => `plugin${i}`).join(', ');
  const content = `${GENERATED_MARKER}import type { PluginManifest } from './registry';\n${imports}\n\nexport const manifests: PluginManifest[] = [${arr}];\n`;
  fs.writeFileSync(MANIFESTS_FILE, content);
}

// Main
cleanPreviousRoutes();

const generatedRoutes = [];
const pluginNames = [];
const pluginDirs = fs.readdirSync(PLUGINS_DIR).filter((d) => {
  const full = path.join(PLUGINS_DIR, d);
  return fs.statSync(full).isDirectory() && !d.startsWith('_') && d !== 'node_modules';
});

for (const pluginName of pluginDirs) {
  const pluginDir = path.join(PLUGINS_DIR, pluginName);
  const manifest = loadManifest(pluginDir);
  if (!manifest) continue;

  pluginNames.push(pluginName);

  const pages = manifest.pages || {};
  const api = manifest.api || {};

  for (const [route, importPath] of Object.entries(pages)) {
    console.log(`  [plugin:${pluginName}] page ${route}`);
    generatePageShim(route, importPath);
    generatedRoutes.push(`${route}/page.tsx`);
  }

  for (const [route, importPath] of Object.entries(api)) {
    console.log(`  [plugin:${pluginName}] api  ${route}`);
    generateApiShim(route, importPath);
    generatedRoutes.push(`${route}/route.ts`);
  }
}

generateManifestsFile(pluginNames);
fs.writeFileSync(MANIFEST_FILE, JSON.stringify({ generatedRoutes }, null, 2));

if (generatedRoutes.length > 0) {
  console.log(`Generated ${generatedRoutes.length} plugin routes.`);
} else {
  console.log('No plugin routes to generate.');
}
